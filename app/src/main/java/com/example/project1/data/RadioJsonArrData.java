package com.example.project1.data;

public class RadioJsonArrData {

    private int Channel;
    private String Url;
    private String ThumbnailText;
    private String FrequencyText;
    private String Glide;

    public int getChannel() {
        return Channel;
    }

    public void setChannel(int channel) {
        Channel = channel;
    }

    public String getUrl() {
        return Url;
    }

    public void setUrl(String url) {
        Url = url;
    }

    public String getThumbnailText() {
        return ThumbnailText;
    }

    public void setThumbnailText(String thumbnailText) {
        ThumbnailText = thumbnailText;
    }

    public String getFrequencyText() {
        return FrequencyText;
    }

    public void setFrequencyText(String frequencyText) {
        FrequencyText = frequencyText;
    }

    public String getGlide() {
        return Glide;
    }

    public void setGlide(String glide) {
        Glide = glide;
    }
}
